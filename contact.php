//Import PHPMailer classes into the global namespace
//These must be at the top of your script, not inside a function
use PHPMailer\PHPMailer\PHPMailer;
use PHPMailer\PHPMailer\SMTP;
use PHPMailer\PHPMailer\Exception;

//Load Composer's autoloader
require 'phpmailer/Exception.php';
require 'phpmailer/PHPMailer.php';
require 'phpmailer/SMTP.php';

if (isset($_POST['submit'])) {
    $name = $_POST['name'];
    $email = $_POST['email'];
    $subject = $_POST['subject'];
    $message = $_POST['message'];

    //Create an instance; passing `true` enables exceptions
    $mail = new PHPMailer(true);

    try {
        //Server settings
        $mail->isSMTP();                                            //Send using SMTP
        $mail->Host       = 'smtp.gmail.com';                     //Set the SMTP server to send through
        $mail->SMTPAuth   = true;                                   //Enable SMTP authentication
        $mail->Username   = 'YOUR-EMAIL-HERE@EXAMPLE.com';                //SMTP username (Replace with your email)
        $mail->Password   = 'YOUR-EMAIL-PASSWORD-HERE';                 //SMTP password (Replace with your email password)
        $mail->SMTPSecure = PHPMailer::ENCRYPTION_SMTPS;            //Enable implicit TLS encryption
        $mail->Port       = 465;                                    //TCP port to connect to; use 587 if you have set `SMTPSecure = PHPMailer::ENCRYPTION_STARTTLS`

        //Recipients
        $mail->setFrom('YOUR-EMAIL-HERE@EXAMPLE.com', 'Contact form');    //Replace with your email
        $mail->addAddress('YOUR-EMAIL-HERE@EXAMPLE.com', 'real estate website'); //Replace with recipient email

        //Content
        $mail->isHTML(true);                                  //Set email format to HTML
        $mail->Subject = 'New Contact Form Submission';
        $mail->Body    = "Sender Name: $name </br></br> Sender Email: $email </br></br> Sender Subject: $subject </br></br> Sender Message: $message";

        $mail->send();
        echo "<script>alert('Your message has been successfully sent.'); window.location.href = 'contact.php';</script>";
    } catch (Exception $e) {
        echo "<script>alert('Message could not be sent. Mailer Error: {$mail->ErrorInfo}'); window.location.href = 'contact.html';</script>";
    }
}
